namespace HotChocolate.Types.RichScalars
{
    public enum Unit
    {
        /// <summary>
        /// The `Seconds` scalar type represents a period of time represented as the total number of seconds.
        /// </summary>
        Seconds,

        /// <summary>
        /// The `Hours` scalar type represents a period of time represented as the total number of hours.
        /// </summary>
        Hours,

        /// <summary>
        /// The `Days` scalar type represents a period of time represented as the total number of days.
        /// </summary>
        Days,

        /// <summary>
        /// The `Degrees` scalar type represents an angle in degrees.
        /// </summary>
        Degrees,

        /// <summary>
        /// The `Radians` scalar type represents an angle in radians.
        /// </summary>
        Radians,

        /// <summary>
        /// "The `Meters` scalar type represents distances measured in meters.
        /// </summary>
        Meters,

        /// <summary>
        /// The `MetersPerSecond` scalar type represents speeds measured in m/s.
        /// </summary>
        MetersPerSecond,

        /// <summary>
        /// The `SquareMeters` scalar type represents areas measured in m^2.
        /// </summary>
        SquareMeters,

        /// <summary>
        /// The `Kilometers` scalar type represents distances measured in kilometers.
        /// </summary>
        Kilometers,

        /// <summary>
        /// The `KilometersPerHour` scalar type represents speeds measured in km/h.
        /// </summary>
        KilometersPerHour,

        /// <summary>
        /// The `SquareKilometers` scalar type represents areas measured in km^2.
        /// </summary>
        SquareKilometers,

        /// <summary>
        /// The `Miles` scalar type represents distances measured in miles.
        /// </summary>
        Miles,

        /// <summary>
        /// The `MilesPerHour` scalar type represents speeds measured in mi/h.
        /// </summary>
        MilesPerHour,

        /// <summary>
        /// The `SquareMiles` scalar type represents areas measured in mi^2.
        /// </summary>
        SquareMiles
    }
}